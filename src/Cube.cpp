void loadTexture(const char *textureName, GLuint *texture)
{
    int width, height, nrChannels;
    unsigned char *data;
    glGenTextures(1, texture);
    glBindTexture(GL_TEXTURE_2D, *texture);

    stbi_set_flip_vertically_on_load(true);
    data = stbi_load(AssetsLoader(textureName).getPath().c_str(), &width, &height, &nrChannels, 0);

    if (data)
    {
        GLenum format;
        if (nrChannels == 1)
            format = GL_RED;
        else if (nrChannels == 3)
            format = GL_RGB;
        else if (nrChannels == 4)
            format = GL_RGBA;
        glTexImage2D(GL_TEXTURE_2D, 0, format, width, height, 0, format, GL_UNSIGNED_BYTE, data);
        glGenerateMipmap(GL_TEXTURE_2D);

        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
    }
    else
    {
        std::cout << "ERROR::Failed to load texture." << std::endl;
    }
    stbi_image_free(data);
}

Cube::Cube(const char *diffuse, const char *specular)
{

    loadModel();

    if (diffuse)
        loadTexture(diffuse, &diffuseMap);
    if (specular)
        loadTexture(specular, &specularMap);

    program = new Program("cube.vs", "cube.fs");
}

Cube::~Cube()
{
    glDeleteVertexArrays(1, &VAO);
    glDeleteBuffers(1, &VBO);
}

void Cube::loadModel()
{
    glGenVertexArrays(1, &VAO);
    glBindVertexArray(VAO);

    glGenBuffers(1, &VBO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);

    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void *)0);
    glEnableVertexAttribArray(0);

    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void *)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);

    glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void *)(6 * sizeof(float)));
    glEnableVertexAttribArray(2);
}

void Cube::render(Camera *camera)
{

    glm::mat4 model = glm::mat4(1.0f); // make sure to initialize matrix to identity matrix first
    glm::mat4 view = glm::mat4(1.0f);
    glm::mat4 projection = glm::mat4(1.0f);
    model = glm::scale(model, scale);
    model = glm::rotate(model, rotation.x, vec3(1.0f, 0.0f, 0.0f));
    model = glm::rotate(model, rotation.y, vec3(0.0f, 1.0f, 0.0f));
    model = glm::rotate(model, rotation.z, vec3(0.0f, 0.0f, 1.0f));
    model = glm::translate(model, location);

    program->use();
    program->setUniform("model", glm::value_ptr(model));
    program->setUniform("view", glm::value_ptr(camera->getViewMatrix()));
    program->setUniform("projection", glm::value_ptr(camera->getProjectionMatrix()));

    if (diffuseMap)
    {
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D, diffuseMap);
    }
    if (specularMap)
    {
        glActiveTexture(GL_TEXTURE1);
        glBindTexture(GL_TEXTURE_2D, specularMap);
    }
    glBindVertexArray(VAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);

    glBindVertexArray(0);
}

